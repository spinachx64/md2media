function rangesForPeriod(period, lastDay) {
  if (period === 'early') return [[1, 3], [4, 7], [8, 10]];
  if (period === 'mid') return [[11, 13], [14, 17], [18, 20]];
  const length = lastDay - 20;
  const firstEnd = 20 + Math.round(length / 3);
  const secondEnd = 20 + Math.round(length * 2 / 3);
  return [[21, firstEnd], [firstEnd + 1, secondEnd], [secondEnd + 1, lastDay]];
}

export function resolveSeasonalTheme(date = new Date()) {
  if (!(date instanceof Date) || Number.isNaN(date.getTime())) throw new TypeError('日期必须是有效的 Date 对象');
  const month = date.getMonth() + 1;
  const day = date.getDate();
  const period = day <= 10 ? 'early' : day <= 20 ? 'mid' : 'late';
  const lastDay = new Date(date.getFullYear(), month, 0).getDate();
  const ranges = rangesForPeriod(period, lastDay);
  const phaseIndex = ranges.findIndex(([start, end]) => day >= start && day <= end);
  const result = seasonalThemeSchedule.find((item) => item.month === month && item.period === period && item.phaseIndex === phaseIndex);
  if (!result) throw new Error(`未找到季节主题：${month} 月${period} 第 ${phaseIndex + 1} 段`);
  const [startDay, endDay] = ranges[phaseIndex];
  return { ...result, startDay, endDay };
}
